// DOM Elements
var modal, modalContent, closeBtn;
var modalPositionImg, modalThrowImg;
var modalPositionCircle, modalThrowCircle;
var modalInstructions;

// Initialize application
document.addEventListener("DOMContentLoaded", function () {
  initializeModal();
  initializePositionItems();
  initializeSmoothScrolling();

  console.log("CS Smoke Guide initialized");
});

// Initialize modal functionality
function initializeModal() {
  modal = document.getElementById("smoke-detail-modal");
  closeBtn = modal.querySelector(".close");
  modalPositionImg = document.getElementById("modal-position-img");
  modalThrowImg = document.getElementById("modal-throw-img");
  modalPositionCircle = document.getElementById("modal-position-circle");
  modalThrowCircle = document.getElementById("modal-throw-circle");
  modalInstructions = document.getElementById("modal-instructions");

  // Close modal events
  closeBtn.addEventListener("click", closeModal);
  modal.addEventListener("click", function (e) {
    if (e.target === modal) {
      closeModal();
    }
  });

  // Close modal on Escape key
  document.addEventListener("keydown", function (e) {
    if (e.key === "Escape" && modal.classList.contains("show")) {
      closeModal();
    }
  });
}

// Initialize position items click handlers
function initializePositionItems() {
  var positionItems = document.querySelectorAll(".position-item");

  for (var i = 0; i < positionItems.length; i++) {
    (function (item) {
      item.addEventListener("click", function () {
        var smokeId = this.getAttribute("data-smoke");
        openSmokeDetail(smokeId);

        // Add active state
        for (var j = 0; j < positionItems.length; j++) {
          positionItems[j].classList.remove("active");
        }
        this.classList.add("active");
      });

      // Add hover effects
      item.addEventListener("mouseenter", function () {
        this.style.transform = "translateY(-8px) scale(1.02)";
      });

      item.addEventListener("mouseleave", function () {
        if (!this.classList.contains("active")) {
          this.style.transform = "";
        }
      });
    })(positionItems[i]);
  }
}

// Open smoke detail modal
function openSmokeDetail(smokeId) {
  var smokeInfo = smokeData[smokeId];

  if (!smokeInfo) {
    console.error("Smoke data not found for:", smokeId);
    return;
  }

  // Set images
  modalPositionImg.src = smokeInfo.position;
  modalThrowImg.src = smokeInfo.throw;

  // Set circle positions
  setCirclePosition(modalPositionCircle, smokeInfo.positionCircle);
  setCirclePosition(modalThrowCircle, smokeInfo.throwCircle);

  // Set instructions
  modalInstructions.innerHTML = smokeInfo.instructions;

  // Show modal
  modal.classList.add("show");
  document.body.style.overflow = "hidden";

  console.log("Opened smoke detail for:", smokeId);
}

// Close modal
function closeModal() {
  modal.classList.remove("show");
  document.body.style.overflow = "";

  // Remove active states
  var activeItems = document.querySelectorAll(".position-item.active");
  for (var i = 0; i < activeItems.length; i++) {
    activeItems[i].classList.remove("active");
    activeItems[i].style.transform = "";
  }

  console.log("Modal closed");
}

// Set circle position
function setCirclePosition(circle, position) {
  circle.style.top = position.top;
  circle.style.left = position.left;
}

// Initialize smooth scrolling for navigation links
function initializeSmoothScrolling() {
  var navLinks = document.querySelectorAll(".nav-links a");

  for (var i = 0; i < navLinks.length; i++) {
    (function (link) {
      link.addEventListener("click", function (e) {
        var href = this.getAttribute("href");

        if (href.indexOf("#") === 0) {
          e.preventDefault();

          var targetId = href.substring(1);
          var targetElement = document.getElementById(targetId);

          if (targetElement) {
            var headerHeight = 120;
            var targetPosition = targetElement.offsetTop - headerHeight;

            window.scrollTo({
              top: targetPosition,
              behavior: "smooth",
            });

            // Update active navigation
            updateActiveNavigation(targetId);
          }
        }
      });
    })(navLinks[i]);
  }
}

// Update active navigation item
function updateActiveNavigation(activeId) {
  var navLinks = document.querySelectorAll(".nav-links a");

  for (var i = 0; i < navLinks.length; i++) {
    var link = navLinks[i];
    var href = link.getAttribute("href");
    if (href === "#" + activeId) {
      link.style.color = "#ff6b35";
      link.style.backgroundColor = "rgba(255, 107, 53, 0.1)";
    } else {
      link.style.color = "";
      link.style.backgroundColor = "";
    }
  }

  // Reset after 3 seconds
  setTimeout(function () {
    for (var i = 0; i < navLinks.length; i++) {
      navLinks[i].style.color = "";
      navLinks[i].style.backgroundColor = "";
    }
  }, 3000);
}

// Handle image loading errors
function handleImageError(img) {
  img.style.display = "none";
  console.error("Failed to load image:", img.src);
}

// Add error handling for all images
document.addEventListener("DOMContentLoaded", function () {
  var images = document.querySelectorAll("img");
  for (var i = 0; i < images.length; i++) {
    (function (img) {
      img.addEventListener("error", function () {
        handleImageError(this);
      });
    })(images[i]);
  }
});

// Scroll to top functionality
function scrollToTop() {
  window.scrollTo({
    top: 0,
    behavior: "smooth",
  });
}

// Add scroll to top button
function addScrollToTopButton() {
  var scrollBtn = document.createElement("button");
  scrollBtn.innerHTML = "↑";
  scrollBtn.className = "scroll-to-top";
  scrollBtn.style.cssText =
    "position: fixed; bottom: 30px; right: 30px; width: 50px; height: 50px; border: none; border-radius: 50%; background-color: #ff6b35; color: white; font-size: 24px; font-weight: bold; cursor: pointer; z-index: 1000; opacity: 0; visibility: hidden; transition: all 0.3s ease; box-shadow: 0 4px 15px rgba(255, 107, 53, 0.3);";

  scrollBtn.addEventListener("click", scrollToTop);
  document.body.appendChild(scrollBtn);

  // Show/hide on scroll
  window.addEventListener("scroll", function () {
    if (window.pageYOffset > 300) {
      scrollBtn.style.opacity = "1";
      scrollBtn.style.visibility = "visible";
    } else {
      scrollBtn.style.opacity = "0";
      scrollBtn.style.visibility = "hidden";
    }
  });
}

// Initialize scroll to top button
document.addEventListener("DOMContentLoaded", function () {
  setTimeout(addScrollToTopButton, 1000);
});

